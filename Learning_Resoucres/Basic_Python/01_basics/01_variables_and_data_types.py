# Numbers
x = 10
y = 3.14
print(x, type(x))
print(y, type(y))

# Strings
name = "Python"
message = 'Hello, World!'
print(name, type(name))
print(message, type(message))

# Boolean
is_true = True
is_false = False
print(is_true, type(is_true))
print(is_false, type(is_false))

# Type conversion
num_str = "123"
num_int = int(num_str)
num_float = float(num_str)
print(num_int, type(num_int))
print(num_float, type(num_float)) 